// n counting down
for(let n = 10; n > 0; n = n - 1){
    console.log("I love Vivas ... please buy some");
}
// n counting up
let n = 0;
while(n < 10){
    console.log("I bought some last week ... Did you eat them already?");
    // if you don't add to n it will always be less than 10
    n = n + 1;
}